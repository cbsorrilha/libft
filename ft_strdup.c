#include"libft.h"

char	*ft_strdup(const char *s1)
{
	size_t	len;
	char	*p;
	int		i;

	len = ft_strlen((char *)s1);
	p = malloc(len + 1);
	if (p == NULL)
		return (NULL);
	i = -1;
	while (s1[++i])
		p[i] = s1[i];
	p[i] = '\0';
	return (p);
}
