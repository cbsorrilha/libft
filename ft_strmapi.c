#include "libft.h"

char	*ft_strmapi(const char *s, char (*f)(unsigned int, char))
{
	size_t	i;
	char	*s2;

	if (!s || !f)
		return (NULL);
	i = 0;
	s2 = ft_calloc((ft_strlen((char *)s) + 1), sizeof (char));
	if (!s2)
		return (NULL);
	while (s[i])
	{
		s2[i] = f(i, s[i]);
		i++;
	}
	s2[i] = '\0';
	return (s2);
}
